# format method

sentence = "The sum of 5 + 10 is {0}".format(50)
print(sentence)

sentence = "Hello {0}".format("Alex")
print(sentence)

sentence = "The sum of {0} + {1} is {2}".format(1,2,3)
print(sentence)

sentence = "{3} {4} {0} {1} {2}".format("you", "must", "decide", "your", "path")
print(sentence)
