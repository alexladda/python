# integer   'int'
# string    'str'
# decimal   'float'
# boolean   'bool'

data_type = type(12)
print(data_type)

data_type = type("hello world")
# dynamic typing
print(data_type)

data_type = type(12.5)
print(data_type)

data_type = type(True)
print(data_type)
# single quotes     ''
# double quotes     ""

sentence = 'I am home'
print (sentence)

sentence = "I'm home"
print (sentence)

sentence = 'I am \n home'
print (sentence)

sentence = "I'm \n home"
print (sentence)
